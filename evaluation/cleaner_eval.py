"""
evaluation/cleaner_eval.py

Evaluation utilities for validating cleaned chemical structure images.
"""

from __future__ import annotations

from pathlib import Path
from typing import Dict

import cv2
import pandas as pd


class CleanerEvaluator:
    """
    Evaluates cleaned images generated by the image cleaning stage.
    """

    REQUIRED_COLUMNS = [
        "image_id",
        "clean_path",
    ]

    VALID_EXTENSIONS = {".png", ".jpg", ".jpeg"}

    def __init__(self, metadata_csv: str | Path):

        self.metadata_csv = Path(metadata_csv)

        if not self.metadata_csv.exists():
            raise FileNotFoundError(
                f"Metadata file not found: {self.metadata_csv}"
            )

        self.df = pd.read_csv(self.metadata_csv)

        self.results: Dict[str, object] = {}

    # ---------------------------------------------------------
    # Column Validation
    # ---------------------------------------------------------

    def validate_columns(self):

        missing = [
            column
            for column in self.REQUIRED_COLUMNS
            if column not in self.df.columns
        ]

        self.results["missing_columns"] = missing

        return len(missing) == 0

    # ---------------------------------------------------------
    # File Existence
    # ---------------------------------------------------------

    def validate_paths(self):

        missing = 0

        for image_path in self.df["clean_path"]:

            if not Path(image_path).exists():
                missing += 1

        self.results["missing_clean_images"] = missing

    # ---------------------------------------------------------
    # Image Readability
    # ---------------------------------------------------------

    def validate_images(self):

        unreadable = 0
        invalid_dimensions = 0

        widths = []
        heights = []

        for image_path in self.df["clean_path"]:

            image = cv2.imread(str(image_path))

            if image is None:
                unreadable += 1
                continue

            h, w = image.shape[:2]

            if h <= 0 or w <= 0:
                invalid_dimensions += 1

            widths.append(w)
            heights.append(h)

        self.results["unreadable_images"] = unreadable
        self.results["invalid_dimensions"] = invalid_dimensions

        if widths:
            self.results["average_width"] = round(sum(widths) / len(widths), 2)
            self.results["average_height"] = round(sum(heights) / len(heights), 2)

    # ---------------------------------------------------------
    # File Format
    # ---------------------------------------------------------

    def validate_formats(self):

        invalid = 0

        for image_path in self.df["clean_path"]:

            suffix = Path(image_path).suffix.lower()

            if suffix not in self.VALID_EXTENSIONS:
                invalid += 1

        self.results["invalid_formats"] = invalid

    # ---------------------------------------------------------
    # File Sizes
    # ---------------------------------------------------------

    def validate_file_sizes(self):

        empty = 0
        total_size = 0
        valid_files = 0

        for image_path in self.df["clean_path"]:

            path = Path(image_path)

            if not path.exists():
                continue

            size = path.stat().st_size

            if size == 0:
                empty += 1
            else:
                total_size += size
                valid_files += 1

        self.results["empty_files"] = empty

        if valid_files:
            self.results["average_file_size_kb"] = round(
                total_size / valid_files / 1024,
                2,
            )

    # ---------------------------------------------------------
    # Summary
    # ---------------------------------------------------------

    def summarize(self):

        self.results["cleaned_images"] = len(self.df)

    # ---------------------------------------------------------
    # Main Evaluation
    # ---------------------------------------------------------

    def evaluate(self):

        if not self.validate_columns():
            return self.results

        self.validate_paths()

        self.validate_images()

        self.validate_formats()

        self.validate_file_sizes()

        self.summarize()

        return self.results

    # ---------------------------------------------------------
    # Export
    # ---------------------------------------------------------

    def to_dataframe(self):

        return pd.DataFrame(
            [
                {
                    "metric": key,
                    "value": value,
                }
                for key, value in self.results.items()
            ]
        )

    def save(self, output_path):

        self.to_dataframe().to_csv(
            output_path,
            index=False,
        )

    # ---------------------------------------------------------
    # Console Output
    # ---------------------------------------------------------

    def print_summary(self):

        print("\nCleaner Evaluation")
        print("-" * 40)

        for key, value in self.results.items():
            print(f"{key:<30}{value}")