"""
evaluation/inventory_eval.py

Evaluation utilities for validating the pipeline inventory.

This module verifies that the generated inventory is complete,
consistent, and ready for downstream processing.
"""

from __future__ import annotations

from pathlib import Path
from typing import Dict, List

import pandas as pd


class InventoryEvaluator:
    """
    Evaluates the inventory CSV generated by the pipeline.
    """

    REQUIRED_COLUMNS = [
        "doc_id",
        "pdf_name",
        "pdf_path",
        "pdf_hash",
        "processed",
    ]

    def __init__(self, inventory_csv: str | Path):
        self.inventory_csv = Path(inventory_csv)

        if not self.inventory_csv.exists():
            raise FileNotFoundError(
                f"Inventory file not found: {self.inventory_csv}"
            )

        self.df = pd.read_csv(self.inventory_csv)

        self.results: Dict[str, object] = {}

    # ------------------------------------------------------------------
    # Validation
    # ------------------------------------------------------------------

    def validate_columns(self) -> List[str]:
        """
        Check that all required columns exist.
        """

        missing = [
            column
            for column in self.REQUIRED_COLUMNS
            if column not in self.df.columns
        ]

        self.results["missing_columns"] = missing

        return missing

    def validate_doc_ids(self) -> int:
        """
        Count duplicate document IDs.
        """

        duplicates = int(
            self.df["doc_id"].duplicated().sum()
        )

        self.results["duplicate_doc_ids"] = duplicates

        return duplicates

    def validate_hashes(self) -> int:
        """
        Count duplicate PDF hashes.
        """

        duplicates = int(
            self.df["pdf_hash"].duplicated().sum()
        )

        self.results["duplicate_hashes"] = duplicates

        return duplicates

    def validate_paths(self) -> int:
        """
        Count missing PDF files.
        """

        missing = 0

        for path in self.df["pdf_path"]:

            if not Path(path).exists():
                missing += 1

        self.results["missing_files"] = missing

        return missing

    def validate_empty_files(self) -> int:
        """
        Count zero-byte PDFs.
        """

        empty = 0

        for path in self.df["pdf_path"]:

            pdf = Path(path)

            if pdf.exists():

                if pdf.stat().st_size == 0:
                    empty += 1

        self.results["empty_files"] = empty

        return empty

    def validate_processed_column(self) -> int:
        """
        Count invalid processed values.
        """

        valid = {True, False, 0, 1}

        invalid = int(
            (~self.df["processed"].isin(valid)).sum()
        )

        self.results["invalid_processed_values"] = invalid

        return invalid

    # ------------------------------------------------------------------
    # Summary
    # ------------------------------------------------------------------

    def summarize(self) -> Dict[str, object]:
        """
        Compute inventory statistics.
        """

        self.results["total_documents"] = len(self.df)

        self.results["processed_documents"] = int(
            self.df["processed"].sum()
        )

        self.results["unprocessed_documents"] = (
            len(self.df)
            - self.results["processed_documents"]
        )

        return self.results

    # ------------------------------------------------------------------
    # Evaluation
    # ------------------------------------------------------------------

    def evaluate(self) -> Dict[str, object]:
        """
        Run all inventory checks.
        """

        self.validate_columns()

        if self.results["missing_columns"]:
            return self.results

        self.validate_doc_ids()

        self.validate_hashes()

        self.validate_paths()

        self.validate_empty_files()

        self.validate_processed_column()

        self.summarize()

        return self.results

    # ------------------------------------------------------------------
    # Export
    # ------------------------------------------------------------------

    def to_dataframe(self) -> pd.DataFrame:
        """
        Convert evaluation results to a DataFrame.
        """

        return pd.DataFrame(
            [
                {
                    "metric": key,
                    "value": value,
                }
                for key, value in self.results.items()
            ]
        )

    def save(self, output_path: str | Path):
        """
        Save evaluation results as CSV.
        """

        output_path = Path(output_path)

        self.to_dataframe().to_csv(
            output_path,
            index=False,
        )

    # ------------------------------------------------------------------
    # Reporting
    # ------------------------------------------------------------------

    def print_summary(self):
        """
        Print evaluation summary.
        """

        print("\nInventory Evaluation")
        print("-" * 40)

        for key, value in self.results.items():
            print(f"{key:<30}{value}")