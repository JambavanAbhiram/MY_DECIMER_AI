"""
evaluation/metadata_eval.py

Evaluation utilities for validating the pipeline metadata.

This evaluator checks the integrity and completeness of the
master metadata table generated by the pipeline.
"""

from __future__ import annotations

from pathlib import Path
from typing import Dict

import pandas as pd


class MetadataEvaluator:
    """
    Evaluates the pipeline metadata table.
    """

    REQUIRED_COLUMNS = [
        "doc_id",
        "pdf_name",
        "page_number",
        "image_id",
        "image_path",
        "clean_path",
        "smiles",
        "processed",
    ]

    def __init__(self, metadata_csv: str | Path):

        self.metadata_csv = Path(metadata_csv)

        if not self.metadata_csv.exists():
            raise FileNotFoundError(
                f"{self.metadata_csv} does not exist."
            )

        self.df = pd.read_csv(self.metadata_csv)

        self.results: Dict[str, object] = {}

    # ---------------------------------------------------------
    # Column Validation
    # ---------------------------------------------------------

    def validate_columns(self):

        missing = [
            c
            for c in self.REQUIRED_COLUMNS
            if c not in self.df.columns
        ]

        self.results["missing_columns"] = missing

        return len(missing) == 0

    # ---------------------------------------------------------
    # Duplicate IDs
    # ---------------------------------------------------------

    def validate_duplicate_doc_ids(self):

        duplicates = int(
            self.df["doc_id"].duplicated().sum()
        )

        self.results["duplicate_doc_ids"] = duplicates

    def validate_duplicate_image_ids(self):

        duplicates = int(
            self.df["image_id"].duplicated().sum()
        )

        self.results["duplicate_image_ids"] = duplicates

    # ---------------------------------------------------------
    # Missing Values
    # ---------------------------------------------------------

    def validate_missing_paths(self):

        image_missing = int(
            self.df["image_path"].isna().sum()
        )

        clean_missing = int(
            self.df["clean_path"].isna().sum()
        )

        self.results["missing_image_paths"] = image_missing
        self.results["missing_clean_paths"] = clean_missing

    def validate_missing_smiles(self):

        missing = int(
            self.df["smiles"].isna().sum()
        )

        empty = int(
            (self.df["smiles"] == "").sum()
        )

        self.results["missing_smiles"] = missing
        self.results["empty_smiles"] = empty

    # ---------------------------------------------------------
    # Processed Flag
    # ---------------------------------------------------------

    def validate_processed(self):

        valid = {True, False, 0, 1}

        invalid = int(
            (~self.df["processed"].isin(valid)).sum()
        )

        self.results["invalid_processed_flags"] = invalid

    # ---------------------------------------------------------
    # Page Numbers
    # ---------------------------------------------------------

    def validate_pages(self):

        invalid = int(
            (self.df["page_number"] < 1).sum()
        )

        self.results["invalid_page_numbers"] = invalid

    # ---------------------------------------------------------
    # File Existence
    # ---------------------------------------------------------

    def validate_files(self):

        missing_images = 0
        missing_clean = 0

        for path in self.df["image_path"]:

            if pd.notna(path):

                if not Path(path).exists():
                    missing_images += 1

        for path in self.df["clean_path"]:

            if pd.notna(path):

                if not Path(path).exists():
                    missing_clean += 1

        self.results["missing_image_files"] = missing_images
        self.results["missing_clean_files"] = missing_clean

    # ---------------------------------------------------------
    # Summary
    # ---------------------------------------------------------

    def summarize(self):

        total = len(self.df)

        complete = self.df.dropna().shape[0]

        completeness = (
            (complete / total) * 100
            if total
            else 0
        )

        self.results["total_records"] = total

        self.results["complete_records"] = complete

        self.results["metadata_completeness"] = round(
            completeness,
            2,
        )

    # ---------------------------------------------------------
    # Main Evaluation
    # ---------------------------------------------------------

    def evaluate(self):

        if not self.validate_columns():
            return self.results

        self.validate_duplicate_doc_ids()

        self.validate_duplicate_image_ids()

        self.validate_missing_paths()

        self.validate_missing_smiles()

        self.validate_processed()

        self.validate_pages()

        self.validate_files()

        self.summarize()

        return self.results

    # ---------------------------------------------------------
    # Export
    # ---------------------------------------------------------

    def to_dataframe(self):

        return pd.DataFrame(
            [
                {
                    "metric": k,
                    "value": v,
                }
                for k, v in self.results.items()
            ]
        )

    def save(self, output_path):

        self.to_dataframe().to_csv(
            output_path,
            index=False,
        )

    # ---------------------------------------------------------
    # Console Output
    # ---------------------------------------------------------

    def print_summary(self):

        print("\nMetadata Evaluation")
        print("-" * 45)

        for k, v in self.results.items():
            print(f"{k:<35}{v}")