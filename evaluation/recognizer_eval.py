"""
evaluation/recognizer_eval.py

Evaluation utilities for validating DECIMER recognition outputs.

Version 1 validates the integrity of predicted SMILES strings.
Ground-truth comparison metrics are added later.
"""

from __future__ import annotations

from pathlib import Path
from typing import Dict

import pandas as pd
from rdkit import Chem


class RecognizerEvaluator:
    """
    Evaluates predicted SMILES generated by the recognizer.
    """

    REQUIRED_COLUMNS = [
        "image_id",
        "smiles",
    ]

    def __init__(self, metadata_csv: str | Path):

        self.metadata_csv = Path(metadata_csv)

        if not self.metadata_csv.exists():
            raise FileNotFoundError(
                f"{self.metadata_csv} does not exist."
            )

        self.df = pd.read_csv(self.metadata_csv)

        self.results: Dict[str, object] = {}

    # ---------------------------------------------------------
    # Column Validation
    # ---------------------------------------------------------

    def validate_columns(self):

        missing = [
            c
            for c in self.REQUIRED_COLUMNS
            if c not in self.df.columns
        ]

        self.results["missing_columns"] = missing

        return len(missing) == 0

    # ---------------------------------------------------------
    # Missing SMILES
    # ---------------------------------------------------------

    def validate_missing_predictions(self):

        missing = int(
            self.df["smiles"].isna().sum()
        )

        empty = int(
            (self.df["smiles"] == "").sum()
        )

        self.results["missing_predictions"] = missing
        self.results["empty_predictions"] = empty

    # ---------------------------------------------------------
    # Valid SMILES
    # ---------------------------------------------------------

    def validate_smiles(self):

        valid = 0
        invalid = 0

        for smiles in self.df["smiles"]:

            if pd.isna(smiles):
                continue

            if smiles == "":
                continue

            molecule = Chem.MolFromSmiles(smiles)

            if molecule is None:
                invalid += 1
            else:
                valid += 1

        self.results["valid_smiles"] = valid
        self.results["invalid_smiles"] = invalid

    # ---------------------------------------------------------
    # Duplicate Predictions
    # ---------------------------------------------------------

    def validate_duplicates(self):

        duplicates = int(
            self.df["smiles"].duplicated().sum()
        )

        self.results["duplicate_predictions"] = duplicates

    # ---------------------------------------------------------
    # Statistics
    # ---------------------------------------------------------

    def summarize(self):

        total = len(self.df)

        valid = self.results.get("valid_smiles", 0)

        success = (
            (valid / total) * 100
            if total
            else 0
        )

        self.results["recognized_structures"] = total

        self.results["recognition_success_rate"] = round(
            success,
            2,
        )

    # ---------------------------------------------------------
    # Main Evaluation
    # ---------------------------------------------------------

    def evaluate(self):

        if not self.validate_columns():
            return self.results

        self.validate_missing_predictions()

        self.validate_smiles()

        self.validate_duplicates()

        self.summarize()

        return self.results

    # ---------------------------------------------------------
    # Export
    # ---------------------------------------------------------

    def to_dataframe(self):

        return pd.DataFrame(
            [
                {
                    "metric": k,
                    "value": v,
                }
                for k, v in self.results.items()
            ]
        )

    def save(self, output_path):

        self.to_dataframe().to_csv(
            output_path,
            index=False,
        )

    # ---------------------------------------------------------
    # Console Output
    # ---------------------------------------------------------

    def print_summary(self):

        print("\nRecognition Evaluation")
        print("-" * 40)

        for k, v in self.results.items():
            print(f"{k:<35}{v}")