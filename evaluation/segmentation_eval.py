"""
evaluation/segmentation_eval.py

Evaluation utilities for validating the segmentation stage.

This evaluator performs integrity checks on the segmented
chemical structure images generated by the pipeline.
"""

from __future__ import annotations

from pathlib import Path
from typing import Dict

import cv2
import pandas as pd


class SegmentationEvaluator:
    """
    Evaluates segmentation outputs generated by the pipeline.
    """

    REQUIRED_COLUMNS = [
        "image_id",
        "image_path",
        "page_number",
    ]

    VALID_EXTENSIONS = {".png", ".jpg", ".jpeg"}

    def __init__(self, metadata_csv: str | Path):

        self.metadata_csv = Path(metadata_csv)

        if not self.metadata_csv.exists():
            raise FileNotFoundError(
                f"Metadata file not found: {self.metadata_csv}"
            )

        self.df = pd.read_csv(self.metadata_csv)

        self.results: Dict[str, object] = {}

    # ---------------------------------------------------------
    # Column Validation
    # ---------------------------------------------------------

    def validate_columns(self):

        missing = [
            column
            for column in self.REQUIRED_COLUMNS
            if column not in self.df.columns
        ]

        self.results["missing_columns"] = missing

        return len(missing) == 0

    # ---------------------------------------------------------
    # File Validation
    # ---------------------------------------------------------

    def validate_image_paths(self):

        missing = 0

        for image_path in self.df["image_path"]:

            if not Path(image_path).exists():
                missing += 1

        self.results["missing_segmented_images"] = missing

    # ---------------------------------------------------------
    # Image Validation
    # ---------------------------------------------------------

    def validate_images(self):

        unreadable = 0
        invalid_dimensions = 0

        widths = []
        heights = []

        for image_path in self.df["image_path"]:

            image = cv2.imread(str(image_path))

            if image is None:
                unreadable += 1
                continue

            h, w = image.shape[:2]

            widths.append(w)
            heights.append(h)

            if h <= 0 or w <= 0:
                invalid_dimensions += 1

        self.results["unreadable_images"] = unreadable
        self.results["invalid_dimensions"] = invalid_dimensions

        if widths:
            self.results["average_width"] = round(sum(widths) / len(widths), 2)
            self.results["average_height"] = round(sum(heights) / len(heights), 2)

    # ---------------------------------------------------------
    # Image Format Validation
    # ---------------------------------------------------------

    def validate_formats(self):

        invalid = 0

        for image_path in self.df["image_path"]:

            suffix = Path(image_path).suffix.lower()

            if suffix not in self.VALID_EXTENSIONS:
                invalid += 1

        self.results["invalid_formats"] = invalid

    # ---------------------------------------------------------
    # Duplicate IDs
    # ---------------------------------------------------------

    def validate_duplicates(self):

        duplicates = int(
            self.df["image_id"].duplicated().sum()
        )

        self.results["duplicate_image_ids"] = duplicates

    # ---------------------------------------------------------
    # Statistics
    # ---------------------------------------------------------

    def summarize(self):

        self.results["segmented_structures"] = len(self.df)

    # ---------------------------------------------------------
    # Main Evaluation
    # ---------------------------------------------------------

    def evaluate(self):

        if not self.validate_columns():
            return self.results

        self.validate_image_paths()

        self.validate_images()

        self.validate_formats()

        self.validate_duplicates()

        self.summarize()

        return self.results

    # ---------------------------------------------------------
    # Export
    # ---------------------------------------------------------

    def to_dataframe(self):

        return pd.DataFrame(
            [
                {
                    "metric": k,
                    "value": v,
                }
                for k, v in self.results.items()
            ]
        )

    def save(self, output_path):

        self.to_dataframe().to_csv(
            output_path,
            index=False,
        )

    # ---------------------------------------------------------
    # Console Output
    # ---------------------------------------------------------

    def print_summary(self):

        print("\nSegmentation Evaluation")
        print("-" * 40)

        for k, v in self.results.items():
            print(f"{k:<30}{v}")