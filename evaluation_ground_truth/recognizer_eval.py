"""
recognizer_eval.py

Core evaluation module for DECIMER Ground Truth Evaluation.

Compares pipeline predictions stored in metadata.csv against
ground truth annotations provided in Excel.
"""

import pandas as pd

from evaluation_ground_truth.utils import canonicalize
from evaluation_ground_truth.metrics import summarize

from evaluation_ground_truth.config import (
    REQUIRED_GT_COLUMNS,
    REQUIRED_METADATA_COLUMNS,
    GT_IMAGE_ID,
    GT_SMILES,
    GT_USE_FOR_EVAL,
    META_IMAGE_ID,
    META_SMILES,
    META_STATUS,
    STATUS_SUCCESS,
)


# =============================================================================
# Validation
# =============================================================================

def validate_columns(df, required_columns, dataframe_name):
    missing = [c for c in required_columns if c not in df.columns]

    if missing:
        raise ValueError(
            f"\n{dataframe_name} is missing required columns:\n"
            + "\n".join(missing)
        )


# =============================================================================
# Load Ground Truth
# =============================================================================

def load_ground_truth(gt_path):

    gt = pd.read_excel(gt_path)

    validate_columns(
        gt,
        REQUIRED_GT_COLUMNS,
        "Ground Truth"
    )

    # Keep the original per-page order before filtering.  Pipeline crop IDs
    # have the form ``<page>_<crop position>``; the Excel IDs (for example,
    # ``IMG00001``) are annotation IDs and cannot be joined to them directly.
    gt["_page_crop_position"] = gt.groupby("page", sort=False).cumcount() + 1

    gt = gt[gt[GT_USE_FOR_EVAL] == True].copy()

    gt[GT_IMAGE_ID] = normalize_image_id(gt[GT_IMAGE_ID])

    return gt


# =============================================================================
# Load Metadata
# =============================================================================

def load_metadata(metadata_path):

    meta = pd.read_csv(metadata_path)

    validate_columns(
        meta,
        REQUIRED_METADATA_COLUMNS,
        "Metadata"
    )

    meta[META_IMAGE_ID] = normalize_image_id(meta[META_IMAGE_ID])

    return meta


def normalize_image_id(values):
    """Return consistently formatted image IDs without converting missing values to 'NAN'."""
    return values.astype("string").str.strip().str.upper()


def make_pipeline_image_ids(gt, meta):
    """Choose the real shared ID, or derive the pipeline crop ID from GT page/order.

    Ground-truth workbooks often use their own annotation IDs (``IMG00001``),
    while metadata uses IDs generated by the detector (``1_1``).  If those
    namespaces do not overlap, use the ground-truth page and its original
    position on that page to form the detector-style key.
    """
    gt_ids = set(gt[GT_IMAGE_ID].dropna())
    meta_ids = set(meta[META_IMAGE_ID].dropna())

    if gt_ids & meta_ids:
        return gt[GT_IMAGE_ID], "ground-truth image_id"

    if "page" not in gt.columns or "page_number" not in meta.columns:
        raise ValueError(
            "Ground-truth image_id values do not match metadata image_id values, "
            "and page-based crop IDs cannot be derived. Expected 'page' in the "
            "ground truth and 'page_number' in metadata."
        )

    page = pd.to_numeric(gt["page"], errors="coerce").astype("Int64")
    crop = gt["_page_crop_position"].astype("Int64")
    return page.astype("string") + "_" + crop.astype("string"), "page/crop position"


# =============================================================================
# Evaluation
# =============================================================================

def evaluate(gt_path, metadata_path):

    print("Loading Ground Truth...")
    gt = load_ground_truth(gt_path)

    print("Loading Metadata...")
    meta = load_metadata(metadata_path)

    gt["_comparison_image_id"], match_strategy = make_pipeline_image_ids(gt, meta)

    print("\n----------------------------------------")
    print("Ground Truth Images :", len(gt))
    print("Metadata Images     :", len(meta))
    print("----------------------------------------")

    # ------------------------------------------------------------------
    # Merge
    # ------------------------------------------------------------------

    print(f"Matching image IDs ({match_strategy})...")

    df = gt.merge(
        meta,
        left_on="_comparison_image_id",
        right_on=META_IMAGE_ID,
        how="left",
        suffixes=("_gt", "_pred"),
        indicator=True
    )

    # Both inputs contain ``image_id``, so pandas applies the merge suffixes.
    # Use the metadata-side name below when deciding whether a prediction row
    # was found; ``image_id`` itself is no longer a column after this merge.
    merged_meta_image_id = f"{META_IMAGE_ID}_pred"

    matched = (df["_merge"] == "both").sum()
    unmatched = (df["_merge"] == "left_only").sum()

    print(f"Matched Images      : {matched}")
    print(f"Unmatched Images    : {unmatched}")

    if unmatched > 0:
        print("\nFirst unmatched image IDs:")

        ids = (
            df.loc[
                df["_merge"] == "left_only",
                "_comparison_image_id"
            ]
            .head(10)
            .tolist()
        )

        for x in ids:
            print("   ", x)

    df.drop(columns="_merge", inplace=True)

    # ------------------------------------------------------------------
    # Canonicalization
    # ------------------------------------------------------------------

    print("\nCanonicalizing SMILES...")

    df["ground_truth_canonical"] = (
        df[GT_SMILES]
        .apply(canonicalize)
    )

    df["predicted_canonical"] = (
        df[META_SMILES]
        .apply(canonicalize)
    )

    # ------------------------------------------------------------------
    # Exact Match
    # ------------------------------------------------------------------

    def normalize(x):

        if pd.isna(x):
            return ""

        return str(x).strip()

    df["exact_match"] = (
        df[GT_SMILES].apply(normalize)
        ==
        df[META_SMILES].apply(normalize)
    )

    df["canonical_match"] = (
        df["ground_truth_canonical"]
        ==
        df["predicted_canonical"]
    )

    # ------------------------------------------------------------------
    # Classification
    # ------------------------------------------------------------------

    print("Classifying predictions...")

    def classify(row):

        status = str(
            row.get(META_STATUS, "")
        ).strip().upper()

        prediction = row.get(META_SMILES)

        # No metadata row matched
        if pd.isna(row.get(merged_meta_image_id)):
            return "Missing Prediction"

        # Empty prediction
        if pd.isna(prediction) or str(prediction).strip() == "":
            return "Missing Prediction"

        # Recognition failed
        if status != STATUS_SUCCESS:
            return "Recognition Failed"

        # Invalid SMILES
        if row["predicted_canonical"] is None:
            return "Invalid SMILES"

        # Correct
        if row["canonical_match"]:
            return "Correct"

        return "Incorrect"

    df["evaluation_status"] = df.apply(
        classify,
        axis=1
    )

    # ------------------------------------------------------------------
    # Diagnostics
    # ------------------------------------------------------------------

    print("\nEvaluation Summary")

    print("-------------------------------")
    print(df["evaluation_status"].value_counts())
    print("-------------------------------")

    print("Generating summary metrics...")

    summary = summarize(df)

    return df, summary
